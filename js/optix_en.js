	function swapback(){
		swapInvImages(keeper[keeper.length-1],aOthers[aOthers.length-1],aLids[aLids.length-1],'go');
		swaptInvImages(tkeeper[tkeeper.length-1],atOthers[atOthers.length-1],atLids[atLids.length-1],'go');
		}
	
	function reset(){
			swapbInvImages(keeper[keeper.length-1],aOthers[aOthers.length-1],aLids[aLids.length-1],'go');
			swaptInvImages(tkeeper[tkeeper.length-1],atOthers[atOthers.length-1],atLids[atLids.length-1],'go');
			keeper[keeper.length-1] = 'space';
			aOthers[aOthers.length-1] = 'space_off';
			aLids[aLids.length-1] = 'trick';		
			}	
			
	function remotechange(sImgName){
		aParts = sImgName.split('_');
		show(aParts[0]);
		sOtherName = sImgName + '_on';
		sTimgName = 'button_' + aParts[0];
		sTotherName = 'button_' + aParts[0] + '_on';
		sTlid = 'navbuttons';
		swaptInvImages(sTimgName, sTotherName, sTlid, 'no');
		swapInvImages(sImgName, sOtherName, aParts[0], 'no');
		tkeeper[tkeeper.length] = sTimgName;
		atOthers[atOthers.length] = 'button_' + aParts[0] + '_off';
		atLids[atLids.length] = sTlid;
		keeper[keeper.length] = sImgName;
		aOthers[aOthers.length] = sImgName + '_off';
		aLids[aLids.length] = aParts[0];
		if(count > 0){
			swaptInvImages(tkeeper[tkeeper.length-1], atOthers[atOthers.length-1], atLids[atLids.length-1], 'go');
			swapInvImages(keeper[keeper.length-1], aOthers[aOthers.length-1], aLids[aLids.length-1], 'go');
			}
		count++;
		}